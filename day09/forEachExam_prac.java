package day09;

public class forEachExam_prac {

	public static void main(String[] args) {
		
        int [] array = {1, 5, 3, 6, 7};
        
        //for each문을 이용해서 array의 값을 한 줄씩 출력하세요
        for (int value : array) {
        	System.out.println(value);
        }
        
    }
}
